package sept.ex_16092024;

public class Lab037 {
    public static void main(String[] args) {
        //compound assignment operator
        int a = 10;
        //  += , _=,*=,/=
        a +=20; // a = a+20
        System.out.println(a);
        a -=20;
        System.out.println(a);
        a /=10;
        System.out.println(a);
        a *=10;
        System.out.println(a);

    }
}
